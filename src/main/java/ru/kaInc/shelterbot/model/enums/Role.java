package ru.kaInc.shelterbot.model.enums;

/**
 * The Role enum represents the different roles that users can have in the system. It defines two roles: "VOLUNTEER" and "USER".
 */
public enum Role {

    /**
     * The "VOLUNTEER" role represents a volunteer user with additional privileges or responsibilities.
     */
    VOLUNTEER,

    /**
     * The "USER" role represents a regular user without special privileges or responsibilities.
     */
    USER
}
